void jogar(palavrT palavr[], int i, int vits)
{
    /*
        Aqui é onde o jogador interage diretamente com o jogo
    */

    char letra;
    char palavrOcult[TAMSTRING];
    char erros[TAMSTRING] = "";
    int valAux_erros = 0;

    int valAux;
    int tam;
    int nivel = 0;
    int i2 = 0;

    /* valAux recebe um valor aleatório entre 0 e i(i foi a quantidade de palavras contadas na função anterior) */
    valAux = rand()%i;
    if(valAux != 0)
        /* Se valAux receber um valor diferente de 0, subtraia menos 1 do mesmo
           Isso foi implementado por conta de erros que podem ocorrer caso seja lido a última linha do arquivo externo */
        valAux = valAux-1;

    tam = strlen(palavr[valAux].palavra); // tam = armazena a quantidade de caracteres da palavra escolhida aleatoriamente
    strcpy(palavrOcult, palavr[valAux].palavra); // Armazena na variável palavrOcult a atual palavra escolhida aleatoriamente na struct palavrT palavr[valAux].palavra
    strset(palavrOcult, '_'); // Substitui todos os caracteres de palavrOcult por '_'

    /* Esse for tem a função de adicionar +1 a cada posição de palavrOcult
       Isso foi implementado com a intenção de excluir o caractere nulo do final da string, impedindo assim que 1 caractere a mais seja contado, e confundindo o jogador */
    for(i2=0; i2<tam; i2++)
    {
        palavrOcult[i2] = palavrOcult[i2 + 1];
    }

    // se for a primeira vez que estiver rodando, renderize a tela inicial...
    if(nivel == 0)
        rend1(palavrOcult, palavr, valAux, nivel);

    // loop infinito
    while(1)
    {
        printf("\tVit%crias consecutivas: %d\t\t\tErros: [%s]\n", 162, vits, erros);
        printf(": ");
        letra = getch(); // armazena um caractere

        if(strchr(palavr[valAux].palavra, letra) == NULL) // se o caractere contido em letra NÃO for encontrado em palavr[valAux].palavra, faça...
        {
            /* Adiciona +1 a variável nivel
               Limpa a tela
               Chama a função rend1 para renderizar a próxima animação */
            nivel++;
            system("cls");
            rend1(palavrOcult, palavr, valAux, nivel);

            //... ainda dentro dessa mesma condição...
            if(strchr(erros,letra) == NULL)
            {
                /* caso o caracter letra não esteja contida na variável erros...
                   adicione esse caracter e uma vírgula
                   Isso tem por função indicar as letras erradas que o usuário entrou */
                erros[valAux_erros] = letra;
                valAux_erros++;
                erros[valAux_erros] = ',';
                valAux_erros++;
            }
        }
        else // se o caractere contido em letra FOI encontrado em palavr[valAux].palavra, faça...
        {
            for(i2=0; i2<tam; i2++) // percorre a string
            {
                if(letra == palavr[valAux].palavra[i2])
                {
                    /* se palavr[valAux].palavra[i2] for igual a letra...
                       palavrOcult recebe o atual caractere de letra na posição i2, substituindo '_' */
                    palavrOcult[i2] = letra;
                    system("cls");
                    rend1(palavrOcult, palavr, valAux, nivel);
                }
            }
        }

        if(strchr(palavrOcult, '_') == NULL)
        {
            /* Se todos os caracteres de palavrOcult forem revelados, o jogador passa para o próximo nível */
            vits++;
            printf("\tVit%crias consecutivas: %d\n", 162, vits);
            printf("Palavra completa!!!\n");

            system("pause");
            system("cls");

            carregardata(vits);
        }
    }

    printf("%s", palavrOcult);
}

void carregardata(int vits)
{
    /*
        Nessa sessão os dados do arquivo externo são trazidos do HD para a memória RAM, possibilitando sua leitura
    */

    palavrT vPal[MAX_PAL];
    FILE *arq;
    int i;

    arq = fopen("data.fc", "rb");

    if(arq == NULL)
    {
        printf("Arquivo data.fc inexistente! Entre na sess%co de configura%c%ces para corrigir o problema!\n\n", 198, 135, 228);
        system("pause");

        system("cls");
        menu();
    }
    else
    {
        for(i=0; !feof(arq) && i<MAX_PAL; i++) // Enquanto i NÃO CHEGAR NO FINAL DO ARQUIVO OU SER MENOR DO QUE A QUANTIDADE MÁXIMA DE PALAVRAS, faça...
        {
            fread(&vPal[i], sizeof(palavrT), 1, arq); // Armazena no endereço da variável vPal[i] os dados do arq
        }

        fclose(arq); // Fecha o arquivo
    }

    jogar(vPal, i, vits);
}

